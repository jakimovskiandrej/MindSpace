/**
 * Анализира текст од дневникот и враќа:
 *  - label: 'positive' | 'neutral' | 'negative'
 *  - score: нумеричка вредност (повисока = попозитивна конотација)
 *  - stressDetected: дали се препознаени зборови поврзани со училишен стрес
 *  - suggestion: кратка, емпатична порака/предлог за корисникот
 */
export function analyzeDiaryEntry(text = '') {
  const lowerText = text.toLowerCase();

  const baseResult = analyzer.analyze(text);

  const stressHits = STRESS_KEYWORDS.filter((word) => lowerText.includes(word));
  const positiveHits = POSITIVE_KEYWORDS.filter((word) => lowerText.includes(word));

  let combinedScore = baseResult.score + positiveHits.length * 2 - stressHits.length * 2;

  let label = 'neutral';
  if (combinedScore > 1) label = 'positive';
  else if (combinedScore < -1) label = 'negative';

  const stressDetected = stressHits.length > 0 || label === 'negative';

  const suggestion = buildSuggestion({ label, stressDetected, stressHits });

  return {
    label,
    score: combinedScore,
    stressDetected,
    matchedStressWords: stressHits,
    suggestion,
  };
}

function buildSuggestion({ label, stressDetected, stressHits }) {
  if (stressDetected) {
    const mentionsBullying = stressHits.some((w) =>
      ['булинг', 'малтретиран', 'малтретирана', 'тепачка', 'изолиран', 'изолирана'].includes(w)
    );
    if (mentionsBullying) {
      return 'Она што го опиша звучи навистина тешко. Те молиме споделeте го ова со училишниот психолог или со некој возрасен на кого му веруваш — никој не треба сам да поминува низ ова.';
    }
    const mentionsExam = stressHits.some((w) => ['испит', 'контролна', 'тест'].includes(w));
    if (mentionsExam) {
      return 'Забележав можен училишен стрес во твојот запис денес. Сакаш ли да пробаш 5-минутна вежба за дишење пред следната контролна?';
    }
    const mentionsSocialMedia = stressHits.some((w) =>
      ['скролам', 'скролање', 'не можам да престанам со телефонот', 'се споредувам', 'фомо', 'лајкови', 'следбеници', 'не добив лајкови'].includes(w)
    );
    if (mentionsSocialMedia) {
      return 'Споредувањето себеси со другите на социјалните мрежи може да биде навистина исцрпувачко. Можеби кратка пауза од телефонот денес ќе помогне — се грижиш за себе кога го забележуваш ова.';
    }
    const mentionsFamily = stressHits.some((w) =>
      ['се карав со родители', 'родителите', 'татко ми се лути', 'мајка ми се лути', 'притисок од родителите', 'разочарав', 'разочарани се'].includes(w)
    );
    if (mentionsFamily) {
      return 'Конфликтите со родителите можат да бидат тешки. Ако сакаш, разговор со училишниот психолог може да помогне да најдеш начин да го изразиш ова.';
    }
    const mentionsBodyImage = stressHits.some((w) =>
      ['дебел сум', 'дебела сум', 'грд сум', 'грда сум', 'мразам како изгледам', 'не сум доволно убав', 'не сум доволно убава'].includes(w)
    );
    if (mentionsBodyImage) {
      return 'Начинот на кој зборуваш за себе денес звучи тежок. Твојата вредност не се мери со изгледот — ако ова чувство трае, поразговарај со училишниот психолог.';
    }
    return 'Забележав малку напнатост во твојот запис денес. Сакаш ли да пробаш кратка вежба за дишење или прошетка без телефон?';
  }
  if (label === 'positive') {
    return 'Убаво е да се види позитивен ден! Продолжи со активностите што те прават да се чувствуваш вака.';
  }
  return 'Фала што го запиша своето размислување денес. Секој ден носи свои предизвици — продолжи да следиш како се чувствуваш.';
}
